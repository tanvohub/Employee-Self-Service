package gov.nysenate.ess.travel.maps;

public class TripDistance {

    private double tripDistanceOut;
    private double tripDistanceTotal;

    public double getTripDistanceOut() {
        return tripDistanceOut;
    }

    public void setTripDistanceOut(double tripDistanceOut) {
        this.tripDistanceOut = tripDistanceOut;
    }

    public double getTripDistanceTotal() {
        return tripDistanceTotal;
    }

    public void setTripDistanceTotal(double tripDistanceTotal) {
        this.tripDistanceTotal = tripDistanceTotal;
    }
}
